    private void promiscuousMode() {
        commander = new FileCommander(fileName, 0, false, deleteWholeBlocks);           //pre visit ineho suboru tu zmen meno
        serializedLength = commander.getSerializedLength();
        commander = new FileCommander(fileName, serializedLength, false, deleteWholeBlocks);     //aj tu a potom este v metode deserialize nizsie
        parentAddress = commander.getRootAddress();
        firstOfChaining = commander.getFirstOfChainedList();
        leafComponentNumber = commander.getLeafComponentNumber();
        dataPartLength = commander.getDataByteArrayLength();
        address = parentAddress;
        empty = commander.getEmpty();

        if (empty.size() == 0) {
            visitorEmpty.setText("-1");
            emptyArrayIndex = -1;
        } else {
            visitorEmpty.setText(empty.get(0) + "");
            emptyArrayIndex = 0;
        }

        visitorParentAddress.setText(parentAddress + "");
        visitorDataPartLength.setText(dataPartLength + "");
        visitorFirstOfChaining.setText(firstOfChaining + "");
        visitorLeafCompNumber.setText(leafComponentNumber + "");
        if (parentAddress != -1) {
            visitorInfoTextArea.setText(deserialize(address));
        } else {
            visitorInfoTextArea.setText("File is empty.");
        }
    }

    public String deserialize(long address) {
        String output = "Address: " + address + "\n";
        output += "Node type: ";                            //o riadok nizsie zmen typ a dlzku serializovaneho kluza na konci
        visit = new BPlusTreeComponent(PropertyKey.class, 0, commander, false, serializedLength, dataPartLength, new PropertyKey().convertedLength());
        visit.deserialize(commander.read(address), address);
        if (visit.getInternal()) {
            output += "internal\n";
        } else {
            output += "leaf\n";
        }

        output += "Record number: " + visit.getRecordNumber() + "\n";
        output += "Records inserted: " + visit.getKeysCount() + "\n";
        output += "Key list: \n";
        for (int i = 0; i < visit.getKeysCount(); i++) {
            output += visit.getKeyAtIndex(i) + "\n";
        }
        output += "\n";
        if (visit.getInternal()) {
            for (int i = 0; i < visit.getKeysCount() + 1; i++) {
                output += visit.getSonAtIndex(i) + "\n";
            }
        }
        output += "\n";
        output += "Parent address: " + visit.getParent() + "\n";
        output += "Right chaining address: " + visit.getRightChaining() + "\n";
        output += "Left chaining address: " + visit.getLeftChaining() + "\n";

        return output;
    }

