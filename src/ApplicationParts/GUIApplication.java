        FMTable.addPropertyChangeListener(new PropertyChangeListener() {

            @Override
            public void propertyChange(PropertyChangeEvent evt) {
                int row = FMTable.getSelectedRow();
                int column = FMTable.getSelectedColumn();
                Object[] temp = applicationLogic.getBorder(row);
                if (column == 0) {
                    return;
                }
                if (column == 1 || column == 3) {

                    if (((Double) applicationLogic.getBorder(row)[column - 1]).compareTo((Double) FMTable.getValueAt(row, column)) == 0) {
                        return;
                    } else {
                        temp[column - 1] = FMTable.getValueAt(row, column);
                    }
                }
                if (column == 2 || column == 4) {
                    if (((String) applicationLogic.getBorder(row)[column - 1]).compareTo((String) FMTable.getValueAt(row, column)) == 0) {
                        return;
                    } else {
                        if (((String) FMTable.getValueAt(row, column)).compareTo("true") == 0 || ((String) FMTable.getValueAt(row, column)).compareTo("false") == 0) {
                            temp[column - 1] = FMTable.getValueAt(row, column);
                        } else {
                            fillTable(applicationLogic.getBorders());
                            return;
                        }
                    }
                }
                if (((String) temp[3]).compareTo("true") == 0) {
                    temp[3] = true;
                } else {
                    temp[3] = false;
                }
                if (((String) temp[1]).compareTo("true") == 0) {
                    temp[1] = true;
                } else {
                    temp[1] = false;
                }
                applicationLogic.updateBorderPoint(row, (Boolean) temp[3], (Double) temp[2], (Boolean) temp[1], (Double) temp[0]);
                fillTable(applicationLogic.getBorders());
            }
        });

